void main() {
  /* Const
   * Never changes
   * Needs to be defined
   */
  const firstName = "Matt";

  /* Final
   * Never changes
   * We can define it later
   */
  final lastName = "Gleich";

  /*
   * You change
   * We can define it later
   */
  var age = 15;

  print(firstName);
  print(lastName);
  print(age);

